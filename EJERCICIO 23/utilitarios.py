def calcularPromedio(val1,val2,val3):
    calculo=val1+val2+val3
    promedio1=calculo/3
    return promedio1
def consultarMulta(numero): 
    if numero == 1:
        return 10 / 100 * 100
    elif numero == 2:
        return 15 / 100 * 100
    elif numero == 3:
        return 20 / 100 * 100
    elif numero == 4:
        return 30 / 100 * 100
    else:
        return -1 
